// FOR
var i;
for (i = 0; i < 5; i++) {
  // Executa 5 vezes, com os valores de passos de 0 a 4.
  console.log('esta no numero: ' + i);
}

// WHILE
n = 0;
while (n < 5) {
  n++;
  console.log('esta no numero: ' + n);
}

// DO WHILE
i = 0;
do {
  i += 1;
  console.log('esta no numero: ' + i);
} while (i < 5);